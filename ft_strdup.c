#include "libft.h"

char	*ft_strdup(const char *s)
{
	char *str;
	int i;

	str = malloc(ft_strlen(s) + 1 * sizeof(char));
	if (!str)
		return (0);
	i = 0;
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	return (str);
}
